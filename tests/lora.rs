#[test]
fn eu868_sf9_crc1() {
    let data = [0x00, 0x00, 0x00, 0x00, 0x02, 0xCC];
    assert_eq!(crc_ccitt::CRC_16_LORA.checksum(&data), 0x7EA2);
}

